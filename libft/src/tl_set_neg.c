#include "libft.h"
#include <stdio.h>

void	tl_set_neg(char **s)
{
	int	i;
	int	dq;
	int	q;

	if (!(*s) || !s || !(*s)[0])
		return ;
	i = 0;
	dq = 0;
	q = 0;
	while (s && (*s) && (*s)[i])
	{
		(*s)[i] == '"' && !(q % 2) ? dq++ : 0;
		(*s)[i] == '\'' && !(dq % 2) ? q++ : 0;
		if ((dq % 2) || (q % 2))
		{
			((*s)[i] == ' ') ? ((*s)[i] = -1) : 0;
			if ((*s)[i] == '\t')
				(*s)[i] = -2;
			if ((*s)[i] == '\n')
				(*s)[i] = -3;
			if ((*s)[i] == ',')
				(*s)[i] = -4;
		}
		i++;
	}
}
